function [zetad, zetaddot1, zetaddot2] = trajectoryplaning(t,tf,x0,y0,z0,xtf,ytf,ztf)
%
xa3to5 = inv(...
[ 3 4*tf 5*tf^2; ...
  6 12*tf 20*tf^2;...
  1 tf tf^2])*[0;0;(xtf-x0)/tf^3];
ya3to5 = inv(...
[ 3 4*tf 5*tf^2; ...
  6 12*tf 20*tf^2;...
  1 tf tf^2])*[0;0;(ytf-y0)/tf^3];
za3to5 = inv(...
[ 3 4*tf 5*tf^2; ...
  6 12*tf 20*tf^2;...
  1 tf tf^2])*[0;0;(ztf-z0)/tf^3];
xd = x0 + xa3to5'*[t^3;t^4;t^5];
yd = y0 + ya3to5'*[t^3;t^4;t^5];
zd = z0 + za3to5'*[t^3;t^4;t^5];
%
zetad=[xd;yd;zd];
zetaddot1 = [3*xa3to5(1,1) 4*xa3to5(2,1) 5*xa3to5(3,1);...
    3*ya3to5(1,1) 4*ya3to5(2,1) 5*ya3to5(3,1);...
    3*za3to5(1,1) 4*za3to5(2,1) 5*za3to5(3,1)]*[t^2;t^3;t^4];
zetaddot2 = [6*xa3to5(1,1) 12*xa3to5(2,1) 20*xa3to5(3,1);...
    6*ya3to5(1,1) 12*ya3to5(2,1) 20*ya3to5(3,1);...
    6*za3to5(1,1) 12*za3to5(2,1) 20*za3to5(3,1)]*[t;t^2;t^3];